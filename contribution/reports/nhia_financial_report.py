def calculate_age(dob, until: None):
    if not until:
        from core import datetime
        until = datetime.datetime.today()
    return until.year - dob.year - ((until.month, until.day) < (dob.month, dob.day))


def nhia_financial_report_query(user, start_date: None, end_date: None, **kwargs):
    # Checking received parameters
    if start_date is None:
        return {"error": "Error - the start date must be selected"}
    if end_date is None:
        return {"error": "Error - the end date must be selected"}
    if start_date > end_date:
        return {"error": "Error - the start date cannot be greater than the end date"}

    header = {
        "start_date": start_date,
        "end_date": end_date,
    }
    report_data = {
        "header": [header],
        "nhia_logo": get_nhia_logo(),
        "requested_by": f"{user.username} - {user.other_names} {user.last_name}",
    }

    # Preparing filters with given parameters and then fetching filtered data
    search_filters = (Q(pay_date__range=(start_date, end_date))
                      & Q(validity_to__isnull=True)
                      & Q(policy__validity_to__isnull=True)
                      & Q(policy__stage=Policy.STAGE_NEW))
    premiums = (Premium.objects.filter(search_filters)
                               .prefetch_related("policy__family")
                               .prefetch_related("policy__family__head_insuree")
                               .prefetch_related("policy__family__location")
                               .prefetch_related("policy__family__location__parent")
                               .prefetch_related("policy__family__location__parent__parent")
                               .order_by("pay_date", "policy__family__head_insuree__chf_id"))

    data = []
    paginator = Paginator(premiums, 1000)
    for page_number in paginator.page_range:
        page = paginator.page(page_number)

        for premium in page.object_list:
            family = premium.policy.family
            insuree = family.head_insuree
            village = family.location
            ward = village.parent
            lga = ward.parent
            new_data_element = {
                "nin": insuree.chf_id,
                "insuree": f"{insuree.other_names} {insuree.last_name}",
                "lga": f"{lga.code} {lga.name}",
                "ward": f"{ward.code} {ward.name}",
                "village": f"{village.code} {village.name}",
                "address": insuree.family.address,
                "gender": insuree.gender_id,
                "age": calculate_age(insuree.dob, premium.pay_date),
                "amount": premium.amount,
                "sector": "Formal" if insuree.is_formal_sector else "Informal",
                "type": premium.pay_type,
                "date": premium.pay_date,
                "receipt": premium.receipt,
            }
            data.append(new_data_element)

    report_data["data"] = data
    return report_data
